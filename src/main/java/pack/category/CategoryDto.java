package pack.category;

public class CategoryDto {
	private String category_id, category_type, category_name, category_regdate, category_update, category_useYN;
	
	public String getCategory_id() {
		return category_id;
	}

	public void setCategory_id(String category_id) {
		this.category_id = category_id;
	}

	public String getCategory_type() {
		return category_type;
	}

	public void setCategory_type(String category_type) {
		this.category_type = category_type;
	}

	public String getCategory_name() {
		return category_name;
	}

	public void setCategory_name(String category_name) {
		this.category_name = category_name;
	}

	public String getCategory_regdate() {
		return category_regdate;
	}

	public void setCategory_regdate(String category_regdate) {
		this.category_regdate = category_regdate;
	}

	public String getCategory_update() {
		return category_update;
	}

	public void setCategory_update(String category_update) {
		this.category_update = category_update;
	}

	public String getCategory_useYN() {
		return category_useYN;
	}

	public void setCategory_useYN(String category_useYN) {
		this.category_useYN = category_useYN;
	}
	
	
}
