package pack.controller;




public class TdBean {
	private String td_title, td_content, td_date;
	private int td_num, td_pn, td_dn;
	
	public String getTd_title() {
		return td_title;
	}
	public void setTd_title(String td_title) {
		this.td_title = td_title;
	}
	public String getTd_content() {
		return td_content;
	}
	public void setTd_content(String td_content) {
		this.td_content = td_content;
	}
	public String getTd_date() {
		return td_date;
	}
	public void setTd_date(String td_date) {
		this.td_date = td_date;
	}
	public int getTd_num() {
		return td_num;
	}
	public void setTd_num(int td_num) {
		this.td_num = td_num;
	}
	public int getTd_pn() {
		return td_pn;
	}
	public void setTd_pn(int td_pn) {
		this.td_pn = td_pn;
	}
	public int getTd_dn() {
		return td_dn;
	}
	public void setTd_dn(int td_dn) {
		this.td_dn = td_dn;
	}
}
